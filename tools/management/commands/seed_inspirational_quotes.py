class Command(BaseCommand):
    help = "Seeds inspirational quotes for Inspirational Cards tool"

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear",
            action="store_true",
            help="Delete existing quotes in language uk before seeding",
        )

    def handle(self, *args, **options):
        if options.get("clear"):
            self.stdout.write("Cleaning up existing inspirational quotes (uk)...")
            InspirationalQuote.objects.filter(language="uk").delete()

        self.stdout.write("Creating inspirational quotes (uk)...")
        created = 0
        for i, q in enumerate(INSPIRING_QUOTES_UK, start=1):
            InspirationalQuote.objects.create(
                author=q["author"],
                text=q["text"],
                language="uk",
                is_active=True,
                sort_order=i,
            )
            created += 1

        self.stdout.write(self.style.SUCCESS(f"Seeded {created} inspirational quotes (uk)."))

