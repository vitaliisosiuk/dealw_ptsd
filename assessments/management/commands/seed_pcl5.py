from assessments.models import Assessment, Question, Choice

def seed_pcl5():
    # Retrieve the PCL-5 assessment (assuming short_name is 'pcl5')
    pcl5_test = Assessment.objects.get(short_name='pcl5')

    # Clear existing questions to avoid duplicates
    pcl5_test.questions.all().delete()

    # PCL-5 Questions
    questions = [
        "Повторювані, тривожні та небажані спогади про стресовий досвід?",
        "Повторювані, тривожні сни про стресовий досвід?",
        "Раптове відчуття або дії так, ніби стресовий досвід відбувається знову (ніби ви знову там)?",
        "Сильне емоційне потрясіння при нагадуванні про стресовий досвід?",
        "Сильні фізичні реакції при нагадуванні про стресовий досвід (наприклад, прискорене серцебиття, пітливість)?",
        "Уникнення думок, почуттів або розмов про стресовий досвід?",
        "Уникнення людей, місць або ситуацій, які нагадують вам про стресовий досвід?",
        "Неможливість згадати важливі частини стресового досвіду?",
        "Сильні негативні переконання про себе, інших або світ (наприклад, 'Я поганий', 'Нікому не можна довіряти')?",
        "Звинувачення себе або інших у тому, що стався стресовий досвід або в його наслідках?",
        "Сильні негативні почуття (наприклад, страх, жах, гнів, провина або сором)?",
        "Втрата інтересу до занять, які раніше приносили задоволення?",
        "Відчуття віддаленості або відірваності від інших людей?",
        "Труднощі з переживанням позитивних почуттів (наприклад, нездатність відчувати щастя або любов)?",
        "Дратівливість, спалахи гніву або агресивна поведінка?",
        "Ризикована або деструктивна поведінка?",
        "Надмірна пильність або постійне очікування небезпеки?",
        "Надмірна реакція на несподівані звуки або події (переляк)?",
        "Труднощі з концентрацією уваги?",
        "Труднощі із засинанням або сном?"
    ]

    options = {
        0: "Зовсім ні",
        1: "Трохи",
        2: "Помірно",
        3: "Досить сильно",
        4: "Дуже сильно"
    }

    for i, text in enumerate(questions):
        q = Question.objects.create(
            assessment=pcl5_test,
            text=text,
            order=i + 1
        )

        for val, opt_text in options.items():
            Choice.objects.create(question=q, value=val, text=opt_text)

    print("Successfully created 20 PCL-5 questions and choices!")

if __name__ == "__main__":
    seed_pcl5()